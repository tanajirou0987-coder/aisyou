// タイプ名からキーワードを取得する関数
export function getTypeKeywords(typeName: string): string[] {
  return drinkingTypeKeywords[typeName] || drinkingTypeKeywords['ミステリアスドリンカー']
}

// 二人の組み合わせからキーワードを取得する関数
export function getCombinationKeywords(type1: string, type2: string): string[] {
  // 同じタイプの場合
  if (type1 === type2) {
    return drinkingTypeKeywords[type1] || []
  }
  
  // 組み合わせパターンを試す
  const combination1 = `${type1}×${type2}`
  const combination2 = `${type2}×${type1}`
  
  if (drinkingTypeKeywords[combination1]) {
    return drinkingTypeKeywords[combination1]
  }
  
  if (drinkingTypeKeywords[combination2]) {
    return drinkingTypeKeywords[combination2]
  }
  
  // 組み合わせが見つからない場合は、両方のキーワードを15個ずつ取得
  const keywords1 = getTypeKeywords(type1).slice(0, 15)
  const keywords2 = getTypeKeywords(type2).slice(0, 15)
  return [...keywords1, ...keywords2]
}
























